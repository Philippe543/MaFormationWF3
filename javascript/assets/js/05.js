//Incrementation et décrementation

/// *********Incrémentation**************************//
var nb1=1;
nb1=nb1+1;

// affichage dans ma console
console.log(nb1);

//Ecriture simplifiée
nb1++;
console.log(nb1);
// nb1++ : Le double ++ permet d'augmenter d'un pas de 1

// ******************Décrémentation*****************//

nb1=nb1-1;
console.log(nb1);

//Ecriture simplifiée
nb1--;
console.log(nb1);
// nb1-- : Le double -- permet de diminuer d'un pas de 1

// ******************subtitilité*****************//

nb1 = 1;
console.log("nb1++ = "+ nb1++);
console.log(nb1);


nb1 = 1;
console.log("++nb1 = "+ ++nb1);